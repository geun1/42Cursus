#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (fd < 0 || !s)
		return ;
	while (*s)
	{
		write(fd, &s[0], 1);
		s++;
	}
	write(fd, "\n", 1);
}
