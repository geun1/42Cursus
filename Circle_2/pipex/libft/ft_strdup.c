#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*cp;
	int		size;
	int		i;

	size = 0;
	while (s1[size])
		size++;
	cp = malloc(sizeof(char) * (size + 1));
	if (cp == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		cp[i] = s1[i];
		i++;
	}
	cp[i] = '\0';
	return (cp);
}
